# you define a variable in Ruby using `=` operator
a = 10

puts a

a = a + 1

puts a

a = "Hello"

puts a

# - Ruby uses Duck Typing: no need to specify the type of the variable
# - You must start with a letter but you can have a number later
# - You should start with a lower case for variables that change
# - You can't have a space in the variable name
# - Convention is to use snake_case for variable names that are composed of
#   multiple words

first_name = "Tam"

puts first_name
